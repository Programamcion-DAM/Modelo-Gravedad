    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String massString = massBox.getText();
        String radiusString = radiusBox.getText();
        String colourString = colorBox.getText();
        String[] colours = colourString.split(",");
        
        float xVelocity;
        float yVelocity;
        
        if(xVelocityBox.getText().equals("")){
            xVelocity = 0;
        }
        else{
            xVelocity = Float.valueOf(xVelocityBox.getText());
        }
        if(yVelocityBox.getText().equals("")){
            yVelocity = 0;
        }
        else{
            yVelocity = Float.valueOf(yVelocityBox.getText());
        }
        
        if(staticCheck.isSelected()){
            p.setStatic(true);
        }else{
            p.setStatic(false);
        }
         
        p.setMass(Long.valueOf(massString).longValue());   
        p.setRadius(Long.valueOf(radiusString).longValue());
        p.setColor(Integer.valueOf(colours[0]), Integer.valueOf(colours[1]), Integer.valueOf(colours[2]));
        p.setVelocity(new PVector(xVelocity,yVelocity));
        
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

